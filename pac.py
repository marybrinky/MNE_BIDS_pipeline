MIN_SHIFT_S = 1.0  # minimum time-shift for surrogate generation (seconds)


# ---------------------------------------------------------------------------
# Load ROI time courses from existing WPLI HDF5
# ---------------------------------------------------------------------------


def _load_tc_from_wpli(
    wpli_file: Path,
) -> tuple[np.ndarray, np.ndarray, list[str], list[str]]:
    """Read stored ROI time courses from a WPLI HDF5 file.

    Parameters
    ----------
    wpli_file : Path   Path to the wpli_painmatrix.h5 produced by wpli.py.

    Returns
    -------
    data       : np.ndarray  shape (n_rois, n_epochs, n_times)
    times      : np.ndarray  shape (n_times,)
    roi_names  : list[str]
    conditions : list[str]   condition label per epoch
    """
    with h5py.File(wpli_file, "r") as f:
        grp = f["roi_time_courses"]
        data = grp["data"][:]
        times = grp["times"][:]
        roi_names = [n.decode() for n in grp["roi_names"][:]]
        conditions = [c.decode() for c in grp["conditions"][:]]
    return data, times, roi_names, conditions


# ---------------------------------------------------------------------------
# Core PAC estimator (Tort et al. 2010)
# ---------------------------------------------------------------------------


def compute_tort_mi(
    phase: np.ndarray, amplitude: np.ndarray, n_bins: int = N_BINS
) -> float:
    """Tort et al. (2010) Modulation Index for 1-D phase and amplitude arrays.

    Parameters
    ----------
    phase     : np.ndarray  Instantaneous phase in radians  (−π … π), length N.
    amplitude : np.ndarray  Instantaneous amplitude envelope, length N.
    n_bins    : int         Number of phase bins (default 18 → 20° resolution).

    Returns
    -------
    float  MI in [0, 1].
    """
    edges = np.linspace(-np.pi, np.pi, n_bins + 1)
    amp_means = np.zeros(n_bins)
    for k in range(n_bins):
        mask = (phase >= edges[k]) & (phase < edges[k + 1])
        if mask.any():
            amp_means[k] = amplitude[mask].mean()

    total = amp_means.sum()
    if total < 1e-12:
        return 0.0

    p = amp_means / total
    p_nz = p[p > 0]
    H = -np.sum(p_nz * np.log(p_nz))
    return float((np.log(n_bins) - H) / np.log(n_bins))


def compute_pac_with_surrogates(
    tc_phase_roi: np.ndarray,
    tc_amp_roi: np.ndarray,
    sfreq: float,
    phase_band: tuple[float, float],
    amp_band: tuple[float, float],
    n_bins: int = N_BINS,
    n_surrogates: int = N_SURROGATES,
    rng_seed: int = 42,
) -> tuple[float, float, float, float]:
    """Compute Tort MI and surrogate z-score for one directed PAC pair.

    Trials are concatenated before MI estimation (more data → more reliable
    phase binning) and the surrogate null distribution is built by randomly
    time-shifting the amplitude envelope on the concatenated signal.

    Parameters
    ----------
    tc_phase_roi : np.ndarray  shape (n_trials, n_times)  —  source of phase
    tc_amp_roi   : np.ndarray  shape (n_trials, n_times)  —  source of amplitude
    sfreq        : float       Sampling frequency in Hz.
    phase_band   : tuple       (lo_hz, hi_hz) for phase extraction.
    amp_band     : tuple       (lo_hz, hi_hz) for amplitude extraction.
    n_bins       : int         Phase bins (default N_BINS).
    n_surrogates : int         Surrogate iterations (default N_SURROGATES).
    rng_seed     : int         NumPy random seed for reproducibility.

    Returns
    -------
    (mi_obs, z_score, surr_mean, surr_std)
    """
    lo_p, hi_p = phase_band
    lo_a, hi_a = amp_band
    n_trials = tc_phase_roi.shape[0]

    # --- per-trial filter + Hilbert → concatenate ---
    phase_list, amp_list = [], []
    for ei in range(n_trials):
        sig_p = _bandpass(tc_phase_roi[ei], lo_p, hi_p, sfreq)
        sig_a = _bandpass(tc_amp_roi[ei], lo_a, hi_a, sfreq)
        phase_list.append(np.angle(hilbert(sig_p)))
        amp_list.append(np.abs(hilbert(sig_a)))

    phase_cat = np.concatenate(phase_list)
    amp_cat = np.concatenate(amp_list)
    n_total = len(phase_cat)

    # --- observed MI ---
    mi_obs = compute_tort_mi(phase_cat, amp_cat, n_bins)

    # --- surrogate distribution (random time-shift of amplitude) ---
    min_shift = max(1, int(MIN_SHIFT_S * sfreq))
    max_shift = n_total - min_shift
    if max_shift <= min_shift:
        # signal too short for meaningful surrogates
        return mi_obs, np.nan, np.nan, np.nan

    rng = np.random.default_rng(rng_seed)
    shifts = rng.integers(min_shift, max_shift, size=n_surrogates)
    surr_mis = np.array(
        [compute_tort_mi(phase_cat, np.roll(amp_cat, sh), n_bins) for sh in shifts]
    )

    surr_mean = surr_mis.mean()
    surr_std = surr_mis.std()
    z = (mi_obs - surr_mean) / surr_std if surr_std > 1e-12 else 0.0

    return float(mi_obs), float(z), float(surr_mean), float(surr_std)


