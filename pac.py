#!/usr/bin/env python3
"""
pac.py
------
Phase-Amplitude Coupling (PAC) between pain matrix regions
for the laser-pain MEG study.

Method: Tort et al. (2010) Modulation Index (MI)
    - Bandpass filter to phase-frequency band  → instantaneous phase (Hilbert)
    - Bandpass filter to amplitude-frequency band → amplitude envelope (Hilbert)
    - Bin phase into N = 18 bins of 20° each  (-π … π)
    - Compute mean amplitude per bin  →  normalise to probability distribution p(φ)
    - MI = (log N − H(p)) / log N      (H = Shannon entropy)
      MI ≈ 0 → uniform / no PAC;  MI → 1 → maximal PAC.

Directionality
    For every ROI pair (A, B) and every (phase_band × amp_band) combination,
    PAC is computed in BOTH directions independently:
        A → B   phase of A modulates amplitude of B
        B → A   phase of B modulates amplitude of A

Statistics
    Surrogate MI values are generated by randomly time-shifting the amplitude
    envelope (N_SURROGATES = 200, minimum shift = 1 s) on the concatenated
    trial signal to build a null distribution.
    Reported: MI_obs (raw),  z = (MI_obs − μ_surr) / σ_surr.

Input
    ROI time courses are read from the WPLI HDF5 file produced by wpli.py
    (group "roi_time_courses") to avoid re-running the inverse.  If the WPLI
    file is absent, use --from-scratch to compute the inverse from epochs.

Output
    derivatives/connectivity/<sub>/task-<task>/
        <sub>_task-<task>_pac_painmatrix.h5

HDF5 layout
    /{phase_roi}_to_{amp_roi}/phase_{band}_amp_{band}/{condition}/
        attrs: mi, z_score, surr_mean, surr_std
    /roi_time_courses/   (data, times, roi_names, conditions)

ROI definitions
    Loaded from core.py ATLAS_CONFIGS, identical to wpli.py.

Reference
    Tort AB et al. (2010). Measuring phase-amplitude coupling between
    neuronal oscillations of different frequencies.
    J Neurophysiol 104(2):1195-210. doi:10.1152/jn.00106.2010

Usage
-----
    python pac.py --subjects 4382
    python pac.py --tasks laser pinprick
    python pac.py --phase-bands theta --amp-bands gamma
    python pac.py --from-scratch          # recompute inverse instead of reading WPLI HDF5
    python pac.py --overwrite
"""

