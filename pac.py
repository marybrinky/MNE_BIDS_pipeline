def pac_one(
    paths: Paths,
    label: str,
    task: str,
    epoch_config: str,
    phase_bands: list[str],
    amp_bands: list[str],
    atlas_key: str = DEFAULT_ATLAS,
    overwrite: bool = False,
    from_scratch: bool = False,
    trial_filter: str = "all",
    logger=None,
) -> bool:
    """Compute PAC for all ROI pairs, directions, and band combinations.

    Parameters
    ----------
    paths        : Paths
    label        : str    Bare subject label.
    task         : str
    epoch_config : str
    phase_bands  : list[str]  Keys from PAC_PHASE_BANDS.
    amp_bands    : list[str]  Keys from PAC_AMP_BANDS.
    atlas_key    : str
    overwrite    : bool
    from_scratch : bool   Recompute inverse rather than loading from WPLI HDF5.
    logger       :

    Returns
    -------
    bool  True on success.
    """
    tag = f"sub-{label} / {task}"
    out_dir = paths.deriv / "connectivity" / sub_id(label) / f"task-{task}"
    suffix   = "" if trial_filter == "all" else f"_{trial_filter}"
    out_file = out_dir / f"{sub_id(label)}_task-{task}_pac_painmatrix{suffix}.h5"

    if _exists(out_file) and not overwrite:
        logger.info("[%s]  SKIP — PAC file exists", tag)
        return True

    # ------------------------------------------------------------------
    # Load ROI time courses
    # ------------------------------------------------------------------
    wpli_file = out_dir / f"{sub_id(label)}_task-{task}_wpli_painmatrix.h5"
    data: np.ndarray | None = None

    if not from_scratch and _exists(wpli_file):
        logger.info("[%s]  Reading ROI time courses from WPLI HDF5 ...", tag)
        try:
            data, times, roi_names, conditions = _load_tc_from_wpli(wpli_file)
            logger.info("[%s]  TC shape: %s  ROIs: %s", tag, data.shape, roi_names)
        except Exception as exc:
            logger.warning(
                "[%s]  Could not read WPLI HDF5 (%s) — falling back to inverse",
                tag,
                exc,
            )
            data = None

    if data is None:
        logger.info("[%s]  Computing ROI time courses from inverse ...", tag)
        inv_file = paths.inv(label, task)
        if not _exists(inv_file):
            logger.warning("[%s]  Inverse operator not found: %s", tag, inv_file)
            return False

        inv = mne.minimum_norm.read_inverse_operator(str(inv_file), verbose=False)
        roi_label_map = load_roi_labels(paths, label, logger, atlas_key=atlas_key)
        if len(roi_label_map) < 2:
            logger.warning("[%s]  Too few ROIs matched — skipping", tag)
            return False

        result = extract_roi_time_courses(
            paths, label, task, epoch_config, inv, roi_label_map, logger,
            trial_filter=trial_filter,
        )
        if result is None:
            return False
        data, times, roi_names, conditions = result

    sfreq = round(1.0 / float(times[1] - times[0]))
    unique_conditions = sorted(set(conditions))
    roi_pairs = _get_roi_pairs(roi_names)
    pac_combos = list(itertools.product(phase_bands, amp_bands))

    n_directions = len(roi_pairs) * 2  # both directions per pair
    logger.info(
        "[%s]  PAC: %d directions × %d band combos × %d conditions",
        tag,
        n_directions,
        len(pac_combos),
        len(unique_conditions),
    )

    # ------------------------------------------------------------------
    # Compute PAC for all directions / band combos / conditions
    # ------------------------------------------------------------------

    # Structure: results[direction][combo][cond] = (mi, z, surr_mean, surr_std)
    pac_results: dict[str, dict[str, dict[str, tuple]]] = {}

    for roi_a, roi_b in roi_pairs:
        if roi_a not in roi_names or roi_b not in roi_names:
            logger.warning("[%s]  Pair %s-%s: ROI missing", tag, roi_a, roi_b)
            continue

        idx_a = roi_names.index(roi_a)
        idx_b = roi_names.index(roi_b)

        # Both directions: A → B  and  B → A
        for phase_roi, amp_roi, phase_idx, amp_idx in [
            (roi_a, roi_b, idx_a, idx_b),
            (roi_b, roi_a, idx_b, idx_a),
        ]:
            direction = f"{phase_roi}_to_{amp_roi}"
            pac_results[direction] = {}

            for p_name, a_name in pac_combos:
                combo_key = f"phase_{p_name}_amp_{a_name}"
                pac_results[direction][combo_key] = {}

                for cond in unique_conditions:
                    mask = np.array([c == cond for c in conditions])
                    tc_ph = data[phase_idx, mask, :].astype(np.float64)
                    tc_am = data[amp_idx, mask, :].astype(np.float64)

                    if tc_ph.shape[0] < 5:
                        logger.warning(
                            "[%s]  %s / %s / %s: only %d trials — skip",
                            tag,
                            direction,
                            combo_key,
                            cond,
                            tc_ph.shape[0],
                        )
                        pac_results[direction][combo_key][cond] = (
                            np.nan,
                            np.nan,
                            np.nan,
                            np.nan,
                        )
                        continue

                    mi, z, surr_mean, surr_std = compute_pac_with_surrogates(
                        tc_ph,
                        tc_am,
                        sfreq,
                        PAC_PHASE_BANDS[p_name],
                        PAC_AMP_BANDS[a_name],
                    )
                    pac_results[direction][combo_key][cond] = (
                        mi,
                        z,
                        surr_mean,
                        surr_std,
                    )
                    logger.info(
                        "[%s]  %-30s  %-30s  %-20s  MI=%.4f  z=%+.2f  (n=%d)",
                        tag,
                        direction,
                        combo_key,
                        cond,
                        mi,
                        z,
                        tc_ph.shape[0],
                    )

    # ------------------------------------------------------------------
    # Save to HDF5
    # ------------------------------------------------------------------
    out_dir.mkdir(parents=True, exist_ok=True)

    def _safe_float(v) -> float:
        return float(v) if (v is not None and not np.isnan(v)) else np.nan

    with h5py.File(out_file, "w") as f:
        f.attrs["subject"] = sub_id(label)
        f.attrs["task"] = task
        f.attrs["epoch_config"] = epoch_config
        f.attrs["atlas"] = atlas_key
        f.attrs["method"] = "PAC-TortMI"
        f.attrs["reference"] = "Tort et al. 2010 J Neurophysiol 104:1195"
        f.attrs["n_bins"] = N_BINS
        f.attrs["n_surrogates"] = N_SURROGATES
        f.attrs["min_shift_s"] = MIN_SHIFT_S
        f.attrs["sfreq"] = sfreq

        for direction, combo_dict in pac_results.items():
            dgrp = f.create_group(direction)
            for combo_key, cond_dict in combo_dict.items():
                cgrp = dgrp.create_group(combo_key)
                for cond, (mi, z, surr_mean, surr_std) in cond_dict.items():
                    sgrp = cgrp.create_group(cond)
                    sgrp.attrs["mi"] = _safe_float(mi)
                    sgrp.attrs["z_score"] = _safe_float(z)
                    sgrp.attrs["surr_mean"] = _safe_float(surr_mean)
                    sgrp.attrs["surr_std"] = _safe_float(surr_std)

        # Store ROI time courses for downstream use (mirrors wpli.py)
        tc_grp = f.create_group("roi_time_courses")
        tc_grp.create_dataset("data", data=data, compression="gzip", compression_opts=4)
        tc_grp.create_dataset("times", data=times)
        tc_grp.create_dataset("roi_names", data=np.array(roi_names, dtype="S"))
        tc_grp.create_dataset("conditions", data=np.array(conditions, dtype="S"))

    logger.info("[%s]  Saved: %s", tag, out_file.name)
    return True


